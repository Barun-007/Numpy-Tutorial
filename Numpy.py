import numpy as np

# Creating array by using list:
x = np.array([33,44,55,22,99])
print(x)
print(type(x))
print(type(x[0]))

# Crating array by Tuple:
arr = np.array((1,3,24,322,3), dtype = np.int16)
print(arr)
print(type(arr[0]))

# Crating array by using arange function():
arr = np.arange(8)
print(arr)
print(type(arr[0]))






